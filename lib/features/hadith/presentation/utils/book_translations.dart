const Map<String, Map<String, String>> _allTranslations = {
  'bukhari': bukhariTranslations,
  'tirmidhi': tirmidhiTranslations,
};

String translateBookTitle(String englishTitle, String collection) {
  final trimmed = englishTitle.trim();
  final map = _allTranslations[collection] ?? tirmidhiTranslations;
  return map[trimmed] ?? _fallbackArabicTitle(trimmed);
}

String _fallbackArabicTitle(String english) {
  final cleaned = english.replaceAll(
      RegExp(
          r'^(The Book on|The Book of|Chapters on|Chapter on|The Book about|The Book of the|Book of)\s+',
          caseSensitive: false),
      '');
  return 'كتاب $cleaned';
}
